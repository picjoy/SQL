SELECT A.FOOD_TYPE, R.REST_ID, R.REST_NAME, A.FAVORITES
FROM (
    SELECT FOOD_TYPE, MAX(FAVORITES) AS FAVORITES
    FROM REST_INFO
    GROUP BY FOOD_TYPE
) A
JOIN REST_INFO R
ON A.FOOD_TYPE = R.FOOD_TYPE AND A.FAVORITES = R.FAVORITES
ORDER BY A.FOOD_TYPE DESC;
